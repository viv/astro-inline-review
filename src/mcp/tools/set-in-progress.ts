import { z } from 'zod';
import type { McpServer } from '@modelcontextprotocol/sdk/server/mcp.js';
import type { ReviewStorage } from '../../server/storage.js';
import type { ToolResult, ErrorResult } from '../types.js';

export async function setInProgressHandler(
  storage: ReviewStorage,
  params: { id: string },
): Promise<ToolResult | ErrorResult> {
  try {
    const store = await storage.mutate(s => {
      const annotation = s.annotations.find(a => a.id === params.id);
      if (!annotation) {
        throw new Error(`Annotation with ID "${params.id}" not found`);
      }

      const now = new Date().toISOString();
      annotation.status = 'in_progress';
      annotation.inProgressAt = now;
      annotation.updatedAt = now;
      return s;
    });

    const annotation = store.annotations.find(a => a.id === params.id);
    return {
      content: [{ type: 'text', text: JSON.stringify(annotation, null, 2) }],
    };
  } catch (err) {
    return {
      isError: true,
      content: [{ type: 'text', text: (err as Error).message }],
    };
  }
}

export function register(server: McpServer, storage: ReviewStorage): void {
  server.tool(
    'set_in_progress',
    'Signal that the agent is about to start working on an annotation. Sets status to "in_progress" so the browser UI shows a working indicator instead of an orphan warning during code edits and hot-reloads. Call this before editing source code, then call resolve_annotation when done.',
    {
      id: z.string().min(1).describe('The annotation ID to mark as in-progress'),
    },
    async (params) => setInProgressHandler(storage, params),
  );
}
